package com.danilo.cursojava.praticas;

public class JavaPratica027 {

	public static void main(String[] args) {
		/* Elabore uma solução para apresentar uma sequencia crescente de valores iniciando com 1
		e finalizando em 50, utilizando a estrutura de repetição com teste no início (while) */
		
		int i = 0;
		while (i < 50) {
			System.out.printf("%d ", ++i);
		}

	}

}
